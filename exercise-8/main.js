let colors =['red', 'white', 'black'];
console.log(colors);

console.log('push')
colors.push('blue');
console.log(colors)

console.log('pop')
colors.pop()
console.log(colors)


console.log('unshift')
colors.unshift('cyan')
console.log(colors)

console.log('shift')
colors.shift()
console.log(colors)


